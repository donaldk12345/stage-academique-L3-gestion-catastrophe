<?php

namespace App\Entity;

use Doctrine\ORM\Mapping\ManyToOne;

class PropertySearch{

    private $nom;
    

    /**
     * Get the value of nom
     */ 
    public function getNom()
    {
        return $this->nom;
    }

    /**
     * Set the value of nom
     *
     * @return  self
     */ 
    public function setNom($nom)
    {
        $this->nom = $nom;

        return $this;
    }

    
}